void main() {
  // Integers: represent whole numbers
  int age = 30;
  int numberOfStudents = 50;

  // Doubles: represent floating-point numbers
  double pi = 3.14;
  double temperature = 25.5;

  // Strings: represent sequences of characters
  String name = "John Doe";
  String message = "Hello, Dart!";

  // Lists: represent ordered collections of objects
  List<int> numbers = [1, 2, 3, 4, 5];
  List<String> fruits = ["apple", "banana", "orange"];

  // Maps: represent key-value pairs
  Map<String, String> capitals = {
    "USA": "Washington D.C.",
    "UK": "London",
    "France": "Paris"
  };
  Map<String, int> ages = {
    "John": 30,
    "Alice": 25,
    "Bob": 35
  };

  // Output examples of each data type
  print("Integers:");
  print("Age: $age, Number of Students: $numberOfStudents");

  print("\nDoubles:");
  print("PI: $pi, Temperature: $temperature");

  print("\nStrings:");
  print("Name: $name, Message: $message");

  print("\nLists:");
  print("Numbers: $numbers, Fruits: $fruits");

  print("\nMaps:");
  print("Capitals: $capitals, Ages: $ages");
}

